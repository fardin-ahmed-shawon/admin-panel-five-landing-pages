    <script>
        // Hero Slider Functionality
        let currentSlide = 0;
        const slides = document.querySelectorAll('.hero-slide');
        const dots = document.querySelectorAll('.slider-dot');
        const totalSlides = slides.length;
        const slidesContainer = document.querySelector('.hero-slides');
        
        // Initialize slider
        function initSlider() {
            updateSlider();
            
            // Auto slide every 5 seconds
            setInterval(() => {
                nextSlide();
            }, 5000);
        }
        
        function nextSlide() {
            currentSlide = (currentSlide + 1) % totalSlides;
            updateSlider();
        }
        
        function prevSlide() {
            currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
            updateSlider();
        }
        
        function goToSlide(index) {
            currentSlide = index;
            updateSlider();
        }
        
        function updateSlider() {
            slidesContainer.style.transform = `translateX(-${currentSlide * 100}%)`;
            
            // Update active dot
            dots.forEach((dot, index) => {
                if (index === currentSlide) {
                    dot.classList.add('active');
                } else {
                    dot.classList.remove('active');
                }
            });
        }
        
        // Event listeners for slider controls
        document.querySelector('.slider-next').addEventListener('click', nextSlide);
        document.querySelector('.slider-prev').addEventListener('click', prevSlide);
        
        dots.forEach(dot => {
            dot.addEventListener('click', function() {
                const index = parseInt(this.getAttribute('data-index'));
                goToSlide(index);
            });
        });
        
        // Initialize slider on page load
        window.addEventListener('DOMContentLoaded', initSlider);

        function scrollToOrder() {
            document.querySelector('.order-form-section').scrollIntoView({ 
                behavior: 'smooth' 
            });
        }

        // Quantity control functionality
        document.querySelectorAll('.quantity-btn').forEach(button => {
            button.addEventListener('click', function() {
                const input = this.parentElement.querySelector('.quantity-input');
                let value = parseInt(input.value);
                
                if (this.classList.contains('plus')) {
                    value++;
                } else if (this.classList.contains('minus') && value > 1) {
                    value--;
                }
                
                input.value = value;
                updateOrderSummary();
            });
        });

        // Product selection functionality
        document.querySelectorAll('.product-checkbox').forEach(checkbox => {
            checkbox.addEventListener('change', function() {
                const card = this.closest('.product-card');
                if (this.checked) {
                    card.classList.add('selected');
                } else {
                    card.classList.remove('selected');
                }
                updateOrderSummary();
            });
        });

        // Update order summary
        function updateOrderSummary() {
            let total = 0;
            
            document.querySelectorAll('.product-checkbox:checked').forEach(checkbox => {
                const card = checkbox.closest('.product-card');
                const quantity = parseInt(card.querySelector('.quantity-input').value);
                const price = 480; // Fixed price for all products
                
                total += price * quantity;
            });
            
            document.querySelector('.order-summary .order-total span:last-child').textContent = `‡ß≥ ${total}`;
            document.querySelectorAll('.order-summary-item:not(.order-total) span:last-child').forEach(span => {
                if (span.textContent.includes('‡¶™‡¶£‡ßç‡¶Ø‡ßá‡¶∞ ‡¶Æ‡ßÇ‡¶≤‡ßç‡¶Ø')) {
                    span.textContent = `‡ß≥ ${total}`;
                }
            });
        }

        // Form submission
        document.querySelector('.submit-button').addEventListener('click', function(e) {
            e.preventDefault();
            
            let isValid = true;
            
            // Simple validation
            const name = document.getElementById('name').value;
            const phone = document.getElementById('phone').value;
            const address = document.getElementById('address').value;
            const district = document.getElementById('district').value;
            
            if (!name.trim()) {
                document.getElementById('nameError').style.display = 'block';
                isValid = false;
            } else {
                document.getElementById('nameError').style.display = 'none';
            }
            
            if (!phone.match(/^(?:\+88|01)?\d{11}$/)) {
                document.getElementById('phoneError').style.display = 'block';
                isValid = false;
            } else {
                document.getElementById('phoneError').style.display = 'none';
            }
            
            if (!address.trim()) {
                document.getElementById('addressError').style.display = 'block';
                isValid = false;
            } else {
                document.getElementById('addressError').style.display = 'none';
            }
            
            if (!district) {
                document.getElementById('districtError').style.display = 'block';
                isValid = false;
            } else {
                document.getElementById('districtError').style.display = 'none';
            }
            
            if (isValid) {
                document.getElementById('successMessage').style.display = 'block';
                setTimeout(() => {
                    document.getElementById('successMessage').style.display = 'none';
                }, 5000);
            }
        });

        // Initialize order summary
        updateOrderSummary();
    </script>
