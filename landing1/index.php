const qtyInput = document.querySelector('td input[type="text"]');

plusBtn.addEventListener('click', () => {
    let qty = parseInt(qtyInput.value);
    qty++;
    qtyInput.value = qty;
    updateOrderSummary();
});

minusBtn.addEventListener('click', () => {
    let qty = parseInt(qtyInput.value);
    if (qty > 1) {
        qty--;
        qtyInput.value = qty;
        updateOrderSummary();
    }
});
